<h3>الاحتفاظ ببياناتك الشخصية</h3>
<p>ستحتفظ الشركة ببياناتك الشخصية فقط طالما كان ذلك ضروريًا للأغراض المنصوص عليها في سياسة الخصوصية هذه. سنحتفظ ببياناتك الشخصية ونستخدمها بالقدر اللازم للامتثال لالتزاماتنا القانونية (على سبيل المثال، إذا طُلب منا الاحتفاظ ببياناتك للامتثال للقوانين المعمول بها)، وحل النزاعات، وإنفاذ اتفاقياتنا وسياساتنا القانونية.</p>
<p>ستحتفظ الشركة أيضًا ببيانات الاستخدام لأغراض التحليل الداخلي. يتم الاحتفاظ ببيانات الاستخدام بشكل عام لفترة زمنية أقصر، باستثناء الحالات التي تُستخدم فيها هذه البيانات لتعزيز الأمان أو تحسين وظائف خدمتنا، أو عندما نكون ملزمين قانونًا بالاحتفاظ بهذه البيانات لفترات زمنية أطول.</p>
<h3>نقل بياناتك الشخصية</h3>
<p>يتم معالجة معلوماتك، بما في ذلك البيانات الشخصية، في المكاتب التشغيلية للشركة وفي أي أماكن أخرى توجد فيها الأطراف المشاركة في المعالجة. هذا يعني أنه قد يتم نقل هذه المعلومات إلى أجهزة كمبيوتر تقع خارج ولايتك أو مقاطعتك أو بلدك أو أي ولاية قضائية حكومية أخرى حيث قد تختلف قوانين حماية البيانات عن تلك الموجودة في ولايتك القضائية.</p>
<p>إن موافقتك على سياسة الخصوصية هذه متبوعة بإرسالك لهذه المعلومات تمثل موافقتك على هذا النقل.</p>
<p>ستتخذ الشركة جميع الخطوات اللازمة بشكل معقول لضمان التعامل مع بياناتك بأمان ووفقًا لسياسة الخصوصية هذه ولن يتم نقل بياناتك الشخصية إلى منظمة أو دولة ما لم تكن هناك ضوابط كافية في مكانها بما في ذلك أمان بياناتك والمعلومات الشخصية الأخرى.</p>
<h3>حذف بياناتك الشخصية</h3>
<p>لديك الحق في حذف أو طلب المساعدة في حذف البيانات الشخصية التي جمعناها عنك.</p>
<p>قد تمنحك خدمتنا القدرة على حذف معلومات معينة عنك من داخل الخدمة.</p>
<p>يمكنك تحديث أو تعديل أو حذف معلوماتك في أي وقت عن طريق تسجيل الدخول إلى حسابك، إذا كان لديك واحد، وزيارة الحساب قسم الإعدادات الذي يسمح لك بإدارة معلوماتك الشخصية.</p>
